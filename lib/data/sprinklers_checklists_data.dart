  JobChecklist(
    id: 'sprinkler_commissioning',
    title: 'Sprinkler system commissioning (BS 9251)',
    category: 'Sprinklers',
    summary:
        'Hydrostatic test, flow test, alarm and pump set commissioning to BS 9251:2021 with full documentation.',
    sections: [
      ChecklistSection('Pre-test isolation', [
        ChecklistItem('Confirm design pack and as-installed drawings on site',
            hint: 'Check head schedule, pipe routes, K factors, calculation set'),
        ChecklistItem('Notify customer and warn of audible alarm test'),
        ChecklistItem('Isolate sprinkler control valves from wholesome supply'),
        ChecklistItem('Lock off the main stop valve to the system',
            hint: 'Tag with installer name and date'),
        ChecklistItem('Check all heads installed correct K factor and temperature',
            hint: 'Cross-reference against the design schedule'),
        ChecklistItem('Confirm escutcheons fitted only after final test'),
      ]),
      ChecklistSection('Hydrostatic pressure test', [
        ChecklistItem('Fill system slowly from lowest point, vent at high points'),
        ChecklistItem('Bring up to 1.5 × maximum working pressure',
            hint: 'Minimum 10 bar test pressure per BS 9251'),
        ChecklistItem('Hold for at least 2 hours with no measurable drop'),
        ChecklistItem('Walk every joint and head with paper towel'),
        ChecklistItem('Record start, end pressure and ambient temperature'),
        ChecklistItem('Depressurise carefully before any rectification'),
      ]),
      ChecklistSection('Flow test from each remote head', [
        ChecklistItem('Identify hydraulically most demanding head from calcs'),
        ChecklistItem('Connect calibrated flow and pressure gauge to test outlet',
            hint: 'Or use orifice plate at the test connection'),
        ChecklistItem('Open test valve and record flow and residual pressure'),
        ChecklistItem('Compare against design flow at design pressure',
            hint: 'Within ±5 % of calculated value'),
        ChecklistItem('Repeat for each zone if zoned'),
        ChecklistItem('Close test valve and confirm system repressurises'),
      ]),
      ChecklistSection('Alarm valve and gong test', [
        ChecklistItem('Open the alarm test bypass valve'),
        ChecklistItem('Confirm motorised water gong sounds within 30 seconds',
            hint: 'Audible at the agreed alarm position'),
        ChecklistItem('Confirm electric flow switch contacts close'),
        ChecklistItem('Verify panel signal at receiver / BMS / sounder'),
        ChecklistItem('Reset and confirm the system goes back to standby'),
      ]),
      ChecklistSection('Pump set commissioning', [
        ChecklistItem('Confirm tank filled and float / level switch works',
            hint: 'Required volume per design × 1.1 safety'),
        ChecklistItem('Run duty pump on test against the test valve'),
        ChecklistItem('Record pump duty, motor amps and discharge pressure'),
        ChecklistItem('Run standby pump on test and confirm auto change-over'),
        ChecklistItem('Test pump-fail and low-water alarm signals'),
        ChecklistItem('Set pressure switches and record settings'),
        ChecklistItem('Confirm any diesel back-up starts and runs'),
      ]),
      ChecklistSection('Documentation BS 9251 certificate', [
        ChecklistItem('Complete BS 9251 design and installation certificate',
            hint: 'Signed by competent designer and installer'),
        ChecklistItem('File hydrostatic and flow test results with calculations'),
        ChecklistItem('Hand over operation and maintenance manual'),
        ChecklistItem('Demonstrate alarm and isolation to the customer'),
        ChecklistItem('Affix log book and weekly check sheet at the riser'),
        ChecklistItem('Notify the alarm receiving centre / building control as required'),
      ]),
