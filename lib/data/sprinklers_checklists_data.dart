      ChecklistSection('Pump test under load', [
        ChecklistItem('Open the test bypass to simulate sprinkler demand'),
        ChecklistItem('Run duty pump for the design duration on test'),
        ChecklistItem('Record discharge pressure and motor current'),
        ChecklistItem('Run standby pump and verify auto change-over works'),
        ChecklistItem('Confirm pump-running, fail and power-loss signals'),
        ChecklistItem('Reset to standby and recheck float levels'),
      ]),
      ChecklistSection('Flow rate verification', [
        ChecklistItem('Open the most remote test outlet'),
        ChecklistItem('Record flow and residual pressure with calibrated gauge'),
